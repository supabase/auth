func join(s ...string) []byte {
	return []byte(strings.Join(s, "\n"))
}

func TestParseCompatibility(t *testing.T) {
	type testCase struct {
		name   string
		from   []byte
		errStr string

		exp, expParse, expGodot map[string]string
	}

	// Each of the `godot - ` tests are copied directly from the godotenv
	// library. Each tests with intentional behavioral changes verifies the
	// old and new behavior.
	//
	// The godotenv portion can be removed in a future pr when we decide
	// to remove the PR. For now it's here to provide peace of mind that
	// best-efforts were made for compatibility.
	tests := []testCase{
		{
			name: "unquoted value beginning with hash is not a comment",
			from: []byte("FOO=#bar"),
			exp:  map[string]string{"FOO": "#bar"},
		},
		{
			name:   "unterminated squote value",
			from:   []byte(`FOO='bar`),
			errStr: "unterminated quoted value",
		},
		{
			name:   "unterminated dquote value",
			from:   []byte(`FOO="bar`),
			errStr: "unterminated quoted value",
		},

		// godot - TestParse
		{
			name: "godot - TestParse",
			from: []byte("ONE=1\nTWO='2'\nTHREE = \"3\""),
			exp: map[string]string{
				"ONE":   "1",
				"TWO":   "2",
				"THREE": "3",
			},
		},

		// godot - TestVariableStringValueSeparator
		{
			name: "godot - TestVariableStringValueSeparator",
			from: []byte("TEST_URLS=\"stratum+tcp://stratum.antpool.com:3333\nstratum+tcp://stratum.antpool.com:443\""),
			exp: map[string]string{
				"TEST_URLS": "stratum+tcp://stratum.antpool.com:3333\nstratum+tcp://stratum.antpool.com:443",
			},
		},

		// godot - tests that use files from fixtures dir
		{
			name: "godot - fixtures/comments.env",
			from: join(
				`# Full line comment`,
				`qux=thud # fred # other`,
				`thud=fred#qux # other`,
				`fred=qux#baz # other # more`,
				`foo=bar # baz`,
				`bar=foo#baz`,
				`baz="foo"#bar`,
			),
			expParse: map[string]string{
				"qux":  "thud",
				"thud": "fred#qux",
				"fred": "qux#baz",
				"foo":  "bar",
				"bar":  "foo#baz",
				"baz":  "foo",
			},

			// Godot v1.5.1 (what we import) had a bug in this test case.
			expGodot: map[string]string{
				"qux":  "thud # fred",
				"thud": "fred#qux",
				"fred": "qux#baz # other",
				"foo":  "bar",
				"bar":  "foo#baz",
				"baz":  "foo",
			},
		},
		{
			name: "godot - fixtures/equals.env",
			from: join(
				`export OPTION_A='postgres://localhost:5432/database?sslmode=disable'`,
			),
			exp: map[string]string{
				"OPTION_A": "postgres://localhost:5432/database?sslmode=disable",
			},
		},
		{
			name: "godot - fixtures/exported.env",
			from: join(
				`export OPTION_A=2`,
				`export OPTION_B='\n'`,
			),
			exp: map[string]string{
				"OPTION_A": "2",
				"OPTION_B": "\\n",
			},
		},
		{
			name: "godot - fixtures/invalid1.env",
			from: join(
				`INVALID LINE`,
				`foo=bar`,
			),
			errStr: `unexpected character`,
		},
		{
			name: "godot - fixtures/plain.env",
			from: join(
				`OPTION_A=1`,
				`OPTION_B=2`,
				`OPTION_C= 3`,
				`OPTION_D =4`,
				`OPTION_E = 5`,
				`OPTION_F = `,
				`OPTION_G=`,
				`OPTION_H=1 2`,
			),
			exp: map[string]string{
				"OPTION_A": "1",
				"OPTION_B": "2",
				"OPTION_C": "3",
				"OPTION_D": "4",
				"OPTION_E": "5",
				"OPTION_F": "",
				"OPTION_G": "",
				"OPTION_H": "1 2",
			},
		},
		{
			name: "godot - fixtures/quoted.env",
			from: join(
				`OPTION_A='1'`,
				`OPTION_B='2'`,
				`OPTION_C=''`,
				`OPTION_D='\n'`,
				`OPTION_E="1"`,
				`OPTION_F="2"`,
				`OPTION_G=""`,
				`OPTION_H="\n"`,
				`OPTION_I = "echo 'asd'"`,
				`OPTION_J='line 1`,
				`line 2'`,
				`OPTION_K='line one`,
				`this is \'quoted\'`,
				`one more line'`,
				`OPTION_L="line 1`,
				`line 2"`,
				`OPTION_M="line one`,
				`this is \"quoted\"`,
				`one more line"`,
			),
			exp: map[string]string{
				"OPTION_A": "1",
				"OPTION_B": "2",
				"OPTION_C": "",
				"OPTION_D": "\\n",
				"OPTION_E": "1",
				"OPTION_F": "2",
				"OPTION_G": "",
				"OPTION_H": "\n",
				"OPTION_I": "echo 'asd'",
				"OPTION_J": "line 1\nline 2",
				"OPTION_K": "line one\nthis is \\'quoted\\'\none more line",
				"OPTION_L": "line 1\nline 2",
				"OPTION_M": "line one\nthis is \"quoted\"\none more line",
			},
		},

		// godot - TestParsing
		//
		// These were generated by adding below in `parseAndCompare`:
		//
		// 	i++
		// 	var sb strings.Builder
		// 	sb.WriteString("{\n")
		// 	sb.WriteString(fmt.Sprintf("name: \"godot - parsing test #%02d\",\n", i))
		// 	sb.WriteString(fmt.Sprintf("from: []byte(%#v),\n", rawEnvLine))
		// 	sb.WriteString(fmt.Sprintf(
		// 		"exp:  map[string]string{%#v: %#v},\n},", expectedKey, expectedValue))
		// 	fmt.Println(sb.String())
		//
		{
			name: "godot - parsing test #01",
			from: []byte("FOO=bar"),
			exp:  map[string]string{"FOO": "bar"},
		},
		{
			name: "godot - parsing test #02",
			from: []byte("FOO =bar"),
			exp:  map[string]string{"FOO": "bar"},
		},
		{
			name: "godot - parsing test #03",
			from: []byte("FOO= bar"),
			exp:  map[string]string{"FOO": "bar"},
		},
		{
			name: "godot - parsing test #04",
			from: []byte("FOO=\"bar\""),
			exp:  map[string]string{"FOO": "bar"},
		},
		{
			name: "godot - parsing test #05",
			from: []byte("FOO='bar'"),
			exp:  map[string]string{"FOO": "bar"},
		},
		{
			name: "godot - parsing test #06",
			from: []byte("FOO=\"escaped\\\"bar\""),
			exp:  map[string]string{"FOO": "escaped\"bar"},
		},
		{
			name: "godot - parsing test #07",
			from: []byte("FOO=\"'d'\""),
			exp:  map[string]string{"FOO": "'d'"},
		},
		{
			name: "godot - parsing test #08",
			from: []byte("OPTION_A: 1"),
			exp:  map[string]string{"OPTION_A": "1"},
		},
		{
			name: "godot - parsing test #09",
			from: []byte("OPTION_A: Foo=bar"),
			exp:  map[string]string{"OPTION_A": "Foo=bar"},
		},
		{
			name: "godot - parsing test #10",
			from: []byte("OPTION_A=1:B"),
			exp:  map[string]string{"OPTION_A": "1:B"},
		},
		{
			name: "godot - parsing test #11",
			from: []byte("export OPTION_A=2"),
			exp:  map[string]string{"OPTION_A": "2"},
		},
		{
			name: "godot - parsing test #12",
			from: []byte("export OPTION_B='\\n'"),
			exp:  map[string]string{"OPTION_B": "\\n"},
		},
		{
			name: "godot - parsing test #13",
			from: []byte("export exportFoo=2"),
			exp:  map[string]string{"exportFoo": "2"},
		},
		{
			name: "godot - parsing test #14",
			from: []byte("exportFOO=2"),
			exp:  map[string]string{"exportFOO": "2"},
		},
		{
			name: "godot - parsing test #15",
			from: []byte("export_FOO =2"),
			exp:  map[string]string{"export_FOO": "2"},
		},
		{
			name: "godot - parsing test #16",
			from: []byte("export.FOO= 2"),
			exp:  map[string]string{"export.FOO": "2"},
		},
		{
			name: "godot - parsing test #17",
			from: []byte("export\tOPTION_A=2"),
			exp:  map[string]string{"OPTION_A": "2"},
		},
		{
			name: "godot - parsing test #18",
			from: []byte("  export OPTION_A=2"),
			exp:  map[string]string{"OPTION_A": "2"},
		},
		{
			name: "godot - parsing test #19",
			from: []byte("\texport OPTION_A=2"),
			exp:  map[string]string{"OPTION_A": "2"},
		},
		{
			name: "godot - parsing test #20",
			from: []byte("FOO=\"bar\\nbaz\""),
			exp:  map[string]string{"FOO": "bar\nbaz"},
		},
		{
			name: "godot - parsing test #21",
			from: []byte("FOO.BAR=foobar"),
			exp:  map[string]string{"FOO.BAR": "foobar"},
		},
		{
			name: "godot - parsing test #22",
			from: []byte("FOO=foobar="),
			exp:  map[string]string{"FOO": "foobar="},
		},
		{
			name: "godot - parsing test #23",
			from: []byte("FOO=bar "),
			exp:  map[string]string{"FOO": "bar"},
		},
		{
			name: "godot - parsing test #24",
			from: []byte("KEY=value value"),
			exp:  map[string]string{"KEY": "value value"},
		},
		{
			name: "godot - parsing test #25",
			from: []byte("FOO=bar # this is foo"),
			exp:  map[string]string{"FOO": "bar"},
		},
		{
			name: "godot - parsing test #26",
			from: []byte("FOO=\"bar#baz\" # comment"),
			exp:  map[string]string{"FOO": "bar#baz"},
		},
		{
			name: "godot - parsing test #27",
			from: []byte("FOO='bar#baz' # comment"),
			exp:  map[string]string{"FOO": "bar#baz"},
		},
		{
			name: "godot - parsing test #28",
			from: []byte("FOO=\"bar#baz#bang\" # comment"),
			exp:  map[string]string{"FOO": "bar#baz#bang"},
		},
		{
			name: "godot - parsing test #29",
			from: []byte("FOO=\"ba#r\""),
			exp:  map[string]string{"FOO": "ba#r"},
		},
		{
			name: "godot - parsing test #30",
			from: []byte("FOO='ba#r'"),
			exp:  map[string]string{"FOO": "ba#r"},
		},
		{
			name: "godot - parsing test #31",
			from: []byte("FOO=\"bar\\n\\ b\\az\""),
			exp:  map[string]string{"FOO": "bar\n baz"},
		},
		{
			name: "godot - parsing test #32",
			from: []byte("FOO=\"bar\\\\\\n\\ b\\az\""),
			exp:  map[string]string{"FOO": "bar\\\n baz"},
		},
		{
			name: "godot - parsing test #33",
			from: []byte("FOO=\"bar\\\\r\\ b\\az\""),
			exp:  map[string]string{"FOO": "bar\\r baz"},
		},
		{
			name: "godot - parsing test #34",
			from: []byte("=\"value\""),
			exp:  map[string]string{"": "value"},
		},
		{
			name: "godot - parsing test #35",
			from: []byte(" KEY =value"),
			exp:  map[string]string{"KEY": "value"},
		},
		{
			name: "godot - parsing test #36",
			from: []byte("   KEY=value"),
			exp:  map[string]string{"KEY": "value"},
		},
		{
			name: "godot - parsing test #37",
			from: []byte("\tKEY=value"),
			exp:  map[string]string{"KEY": "value"},
		},

		// godot - TestLinesToIgnore
		{
			name: "godot - line with nothing but line break",
			from: []byte("\n"),
			exp:  map[string]string{},
		},
		{
			name: "godot - line with nothing but windows-style line break",
			from: []byte("\r\n"),
			exp:  map[string]string{},
		},
		{
			name: "godot - line full of whitespace (tabs)",
			from: []byte("\t\t"),
			exp:  map[string]string{},
		},
		{
			name: "godot - line with comment",
			from: []byte("# Comment"),
			exp:  map[string]string{},
		},
		{
			name: "godot - indented comment",
			from: []byte("\t # comment"),
			exp:  map[string]string{},
		},

		// godot - TestTrailingNewlines
		{
			name: "godot - leading mixed whitespace",
			from: []byte(" \t \t\n\t \t A=a\n"),
			exp:  map[string]string{"A": "a"},
		},
		{
			name: "godot - leading tab",
			from: []byte("\tA=a\n"),
			exp:  map[string]string{"A": "a"},
		},
		{
			name: "godot - leading whitespace",
			from: []byte(" A=a\n"),
			exp:  map[string]string{"A": "a"},
		},
		{
			name: "godot - leading whitespace before export",
			from: []byte(" \t\t export    A=a\n"),
			exp:  map[string]string{"A": "a"},
		},
		{
			name: "godot - no eol",
			from: []byte("A=a"),
			exp:  map[string]string{"A": "a"},
		},
		{
			name: "godot - trailing whitespace",
			from: []byte("A=a \t \t\n"),
			exp:  map[string]string{"A": "a"},
		},
		{
			name: "godot - trailing whitespace with export",
			from: []byte("export A=a\t \t \n"),
			exp:  map[string]string{"A": "a"},
		},
		{
			name: "godot - trailing whitespace with no eol",
			from: []byte("A=a "),
			exp:  map[string]string{"A": "a"},
		},
	}

	runParseTest := func(t *testing.T, tt testCase) {
		check := func(t *testing.T, exp, got map[string]string, err error) {
			t.Helper()
			if tt.errStr != "" {
				require.Error(t, err)
				require.Contains(t, err.Error(), tt.errStr)
				return
			}
			require.NoError(t, err)
			require.Equal(t, exp, got)
		}

		t.Run(tt.name+"/parse", func(t *testing.T) {
			exp := tt.exp
			if tt.expParse != nil {
				exp = tt.expParse
			}
			got, err := ParseData(tt.from)
			check(t, exp, got, err)
		})

		t.Run(tt.name+"/godot", func(t *testing.T) {
			exp := tt.exp
			if tt.expGodot != nil {
				exp = tt.expGodot
			}
			got, err := godotenv.Parse(bytes.NewReader(tt.from))
			check(t, exp, got, err)
		})
	}

	for _, tt := range tests {
		runParseTest(t, tt)
	}
}
